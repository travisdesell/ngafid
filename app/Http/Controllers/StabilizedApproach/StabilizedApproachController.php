    public function chart()
    {
        $rowID     = \Request::query('drill');
        $fleetID = \Auth::user()->org_id;

        $approachData   = StabilizedApproach::select('flight', 'timeOfFinal', 'timeOnFinal')->where('id', '=', $rowID)->first();
        $flightInfo     = FlightID::select('fleet_id', 'time', 'date')->where('id', '=', $approachData['flight'])->first();
        $summary        = 'Flight ID: ' . $approachData['flight'] . '<br>Time of Final: ' . $flightInfo['date'] . ' ' . $approachData['timeOfFinal'] . '<br>Duration: '. $approachData['timeOnFinal'] . ' seconds';


        if ($fleetID == $flightInfo['fleet_id']){ //verification step

            $finalStart = (strtotime($flightInfo['date'] . ' ' . $approachData['timeOfFinal']) - strtotime($flightInfo['date'] . ' ' . $flightInfo['time'])) * 1000;
            $finalEnd   = $finalStart + ($approachData['timeOnFinal'] * 1000);
            $flightID   = $approachData['flight'];
            $flightTime = $flightInfo['time'];

            $chartData = StabilizedApproach::graphApproach($flightID, $flightTime, $finalStart, $finalEnd);

            $series = array();
            $time   = array();
            //$series['type'] = 'line';
            $roll   = array();
            $pitch  = array();
            $ias    = array();
            $vsi    = array();
            foreach($chartData as $chart){
                $roll[]    = (float)$chart->roll; //, 'segmentColor'  => "{$chart->roll_color}"
                $pitch[]   = (float)$chart->pitch; //, 'segmentColor'  => "{$chart->roll_color}"
                $ias[]     = (float)$chart->ias; //, 'segmentColor'  => "{$chart->roll_color}"
                $vsi[]     = (float)$chart->vsi; //, 'segmentColor'  => "{$chart->roll_color}"
                $time[]    = "{$chart->time_sec}";
            }
            //$series['id'] = (int)$rowID;
            $series[] = ['type' => 'spline', 'name' => 'roll', 'data' => $roll, 'color' => 'red', 'marker' => ['symbol' => 'circle', 'radius' => 3]];
            $series[] = ['type' => 'spline', 'name' => 'pitch', 'data' => $pitch, 'color' => 'blue', 'marker' => ['symbol' => 'square', 'radius' => 3]];
            $series[] = ['type' => 'spline', 'name' => 'ias', 'data' => $ias, 'color' => 'green', 'marker' => ['symbol' => 'diamond', 'radius' => 3]];
            $series[] = ['type' => 'spline', 'name' => 'vsi', 'data' => $vsi, 'color' => 'black', 'marker' => ['symbol' => 'triangle-down', 'radius' => 3, 'fillColor' => 'black']];

            /*print_r($time);
            print_r($series);
            die();*/

            return \Response::json(['success' => true,'data' => ['result' => $series, 'time' => $time, 'summary' => $summary] ]);
        }

        //return \Response::json(['success' => true,'data' => ['result' => ''] ]);
    }

